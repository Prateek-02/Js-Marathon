// variables :: a variable is a storage unit to store some data

// var; let; const

// var:- Global scoped, can be redeclared and reassigned
// let:- block scoped, can be reassigned
// const:- block scoped cannot be redeclared or reassigned

//data types:- Number | String | Boolean

var x = 3;
console.log(x);

var y = 4;
console.log(y);

console.log(x+y);

// var variable can be redeclared multiple times
var Uname = 'Prateek';
console.log('Hii,my name is ' + Uname);   // string concatenation
//OR
console.log(`Hii,my name is ${Uname}`);  // template literals

console.log(`Hello World!!`);


